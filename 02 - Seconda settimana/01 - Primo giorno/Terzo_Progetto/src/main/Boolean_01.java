package main;

public class Boolean_01 
{
	public static void main(String[] args) 
	{
		// Dichiarazione
		int numero;
		boolean verifica;
		
		// Inizializzazione
		numero = 10;
		verifica = false;
		
		// Calcolo
		if(numero > 10) 
			verifica = true;
		
		// Output
		System.out.println(verifica);
	}
}
